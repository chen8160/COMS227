package lab3;

public class StringInt {
	public static void main(String[] args) {
		int x = 42;
		String s = "" + x;
		System.out.println(s);
		
		String a = "42";
		int y = Integer.parseInt(a);
		System.out.println(y);
		
		String b = "adf";
		int z = Integer.parseInt(b);
		System.out.println(z);
	}

}
